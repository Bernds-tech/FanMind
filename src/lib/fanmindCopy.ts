export function createFanMindTranslator(language: FanMindLanguage) {
  return (text: string) => (language === "en" ? englishCopy[text] ?? text : text);
}

export function localizeFanMindValue<T>(value: T, translate: (text: string) => string): T {
  if (typeof value === "string") {
    return translate(value) as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => localizeFanMindValue(item, translate)) as T;
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([key, nestedValue]) => [key, localizeFanMindValue(nestedValue, translate)]),
    ) as T;
  }

  return value;
}
