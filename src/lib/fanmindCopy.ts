export type FanMindLanguage = "de" | "en";

export const fanmindLanguages: FanMindLanguage[] = ["de", "en"];

export function getFanMindLanguage(lang?: string | string[] | null): FanMindLanguage {
  const value = Array.isArray(lang) ? lang[0] : lang;
  return value === "en" ? "en" : "de";
}

export function landingPath(language: FanMindLanguage, hash = "") {
  return language === "en" ? `/?lang=en${hash}` : `/${hash}`;
}

export function localizedPath(path: "/login" | "/register" | "/forgot-password" | "/reset-password", language: FanMindLanguage, query = "") {
  const separator = query ? "&" : "?";
  return language === "en" ? `${path}${query}${separator}lang=en` : `${path}${query}`;
}

